using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.ComponentModel.DataAnnotations.Schema;
using System.Linq;
using System.Web;

namespace PHE2.Models
{
    public class BaseEntity
    {
        [Key]
        [Column("guid")]
        public Guid guid { get; set; }

        [Column("created_date")]
